/*
 * i686 PC MBR stage1 loader.
 *
 * Stage2 load metadata is patched into stage1 at install time:
 *   stage2_start_lba:   u32 disk LBA where stage2 begins
 *   stage2_sector_count u16 number of sectors to load
 *
 * Stage1 loads stage2 to 0000:8000 and jumps there.
 */

.code16
.section .mbr, "ax"
.global _start

.equ STAGE2_LOAD,  0x8000

_start:
	cli
	xorw %ax, %ax
	movw %ax, %ds
	movw %ax, %es
	movw %ax, %ss
	movw $0x7c00, %sp
	sti
	cld

	movb %dl, boot_drive

	/* Ensure BIOS INT 13h extensions are available for LBA reads. */
	movb boot_drive, %dl
	movw $0x55aa, %bx
	movb $0x41, %ah
	int $0x13
	jc boot_fail
	cmpw $0xaa55, %bx
	jne boot_fail
	testw $0x0001, %cx
	jz boot_fail

	movl stage2_start_lba, %eax
	testl %eax, %eax
	jz boot_fail
	movl %eax, temp_lba

	movw stage2_sector_count, %ax
	testw %ax, %ax
	jz boot_fail
	movw %ax, stage2_sectors_left

	movw $STAGE2_LOAD, stage2_offset
load_stage2_loop:
	cmpw $0, stage2_sectors_left
	je stage2_jump

	movw stage2_offset, %bx
	movw $1, %cx
	call bios_ext_read
	jc boot_fail

	addw $512, stage2_offset
	incl temp_lba
	decw stage2_sectors_left
	jmp load_stage2_loop

stage2_jump:
	ljmp $0x0000, $STAGE2_LOAD

boot_fail:
	movw $boot_fail_msg, %si
	call print_string
	cli
hang:
	hlt
	jmp hang

/* Print a zero-terminated string at DS:SI using BIOS teletype output. */
print_string:
	lodsb
	testb %al, %al
	jz print_done
	movb $0x0e, %ah
	movb $0x00, %bh
	movb $0x07, %bl
	int $0x10
	jmp print_string
print_done:
	ret

/*
 * Read sectors using INT 13h extensions (AH=42h).
 * Inputs:
 *   temp_lba : u32 start LBA
 *   BX       : destination offset (segment 0000)
 *   CX       : sector count
 * Clobbers: AX, SI
 */
bios_ext_read:
	movw %cx, dap_num_sectors
	movw %bx, dap_buffer_offset
	movw $0, dap_buffer_segment

	movl temp_lba, %eax
	movl %eax, dap_lba_low
	movl $0, dap_lba_high

	movw $dap, %si
	movb $0x42, %ah
	movb boot_drive, %dl
	int $0x13
	ret

boot_drive:
	.byte 0

stage2_patch_signature:
  .ascii "VLSP"
  
stage2_start_lba:
	.long 0

stage2_sector_count:
	.word 0

temp_lba:
	.long 0

stage2_offset:
	.word 0

stage2_sectors_left:
	.word 0

boot_fail_msg:
	.asciz "Failed to boot..."

/* BIOS disk address packet (DAP), 16 bytes. */
dap:
	.byte 0x10
	.byte 0x00
dap_num_sectors:
	.word 0
dap_buffer_offset:
	.word 0
dap_buffer_segment:
	.word 0
dap_lba_low:
	.long 0
dap_lba_high:
	.long 0

/* Keep stage1 in the boot-code area of MBR (446 bytes max). */
.org _start + 446
