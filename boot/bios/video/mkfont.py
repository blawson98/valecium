def FormatCArray(Data, Width=16):
    """Format bytes as a C literal."""
    lines = []
    for i in range(0, len(data), width):
        chunk = data[i : i + width]
        hex_str = ", ".join(f"0x{b:02X}" for b in chunk)
        lines.append(f"    {hex_str},")
    return "\n".join(lines)


def main():
    if len(sys.argv) < 2:
        print(f"Usage: {sys.argv[0]} <font_gen.c>", file=sys.stderr)
        sys.exit(1)

    output = sys.argv[1]

    glyph_lines = []
    for glyph in FONT_DATA:
        glyph_lines.append("    { " + ", ".join(f"0x{b:02X}" for b in glyph) + " },")

    glyph_block = "\n".join(glyph_lines)

    content = f"""// SPDX-License-Identifier: GPL-3.0-only

#include "font.h"

/* Standard VGA 8x16 bitmap font for ASCII 0x20-0x7E.
 * Each glyph is 16 rows x 8 pixels (1 byte per row, MSB = leftmost).
 * Source: IBM VGA ROM font (public-domain equivalent).
 */
const uint8_t g_Font8x16[FONT_NUM][FONT_HEIGHT] = {{
{glyph_block}
}};
"""

    with open(output, "w") as f:
        f.write(content)


if __name__ == "__main__":
    main()
